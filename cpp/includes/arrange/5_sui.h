#ifndef PURPCHI_ZIWEI_5_SUI_H
#define PURPCHI_ZIWEI_5_SUI_H

namespace purpchi{
namespace arrange{

// ============================================
// 长生十二神（长生帝旺系列）
// ============================================

/**
 * 定长生位置（长生十二神的起始位置）
 * 长生十二神顺序: 长生、沐浴、冠带、临官、帝旺、衰、病、死、墓、绝、胎、养
 * @param mingPalace 命宫位置 (0-11)
 * @param wuxingJu 五行局 (2-6)
 * @return 长生位置 (0-11)
 * 
 * 算法:
 * 水二局、火六局 -> 长生在申(6)，顺行
 * 木三局       -> 长生在亥(9)，顺行
 * 金四局       -> 长生在巳(3)，顺行
 * 土五局       -> 长生在申(6)，顺行
 */
int arrangeChangsheng(int mingPalace, int wuxingJu);

// ============================================
// 岁建十二星（岁建系列）
// ============================================

/**
 * 定岁建位置（岁建十二星的起始位置）
 * 岁建十二星顺序: 岁建、晦气、丧门、贯索、官符、小耗、大耗、龙德、白虎、天德、吊客、病符
 * @param yearBranch 年支 (0-11)
 * @return 岁建位置 (0-11)
 * 
 * 算法: 岁建在年支位置，顺行十二星
 */
int arrangeSuijian(int yearBranch);

// ============================================
// 將前十二星（將星系列）
// ============================================

/**
 * 定將星位置（將前十二星的起始位置）
 * 將前十二星顺序: 將星、攀鞍、岁驿、息神、华盖、劫煞、灾煞、天煞、指背、咸池、月煞、亡神
 * @param yearBranch 年支 (0-11)
 * @return 將星位置 (0-11)
 * 
 * 算法: 根据年支三合局
 * 寅午戌年 -> 將星在午(4)
 * 申子辰年 -> 將星在子(10)
 * 巳酉丑年 -> 將星在酉(7)
 * 亥卯未年 -> 將星在卯(1)
 * 顺行十二星
 */
int arrangeJiangxing(int yearBranch);

// ============================================
// 流年十二星（流年系列）
// ============================================

/**
 * 定流年岁前位置（流年十二星的起始位置）
 * 流年十二星顺序: 岁前、岁后、岁中、岁破、岁刃、岁魁、岁德、岁驿、岁煞、岁马、岁禄、岁福
 * @param yearBranch 年支 (0-11)
 * @return 岁前位置 (0-11)
 * 
 * 算法: 岁前在年支前一位（逆行一宫）
 * 其余顺行
 */
int arrangeSuiqian(int yearBranch);

}
}
#endif // PURPCHI_ZIWEI_5_SUI_H